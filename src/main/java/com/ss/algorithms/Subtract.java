package com.ss.algorithms;

public class Subtract {


	private int result;
	
	public Subtract(int firstVal , int secondVal )
	{
		
		setResult(firstVal - secondVal) ;
	}

	
	/**
	 * @return the result
	 */
	public int getResult() {
		return result;
	}

	/**
	 * @param result the result to set
	 */
	public void setResult(int result) {
		this.result = result;
	}
	
	
	

}
